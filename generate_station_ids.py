def check_ace_step():
    import urllib.request
    try:
        req = urllib.request.Request(f"{ACE_STEP_URL}/health")
        with urllib.request.urlopen(req, timeout=5) as resp:
            data = __import__("json").loads(resp.read().decode())
            return data.get("data", {}).get("status") == "ok"
    except Exception:
        return False


def generate_sting(caption, key=None, bpm=None):
    """Generate a short musical sting via ACE-Step. Returns path to wav file."""
    import json
    import time
    import urllib.request

    body = {
        "prompt": caption,
        "lyrics": "",
        "bpm": bpm,
        "key_scale": key or "",
        "audio_duration": random.randint(5, 12),
        "thinking": True,
        "audio_format": "mp3",
    }
    req = urllib.request.Request(
        f"{ACE_STEP_URL}/release_task",
        data=json.dumps(body).encode(),
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=120) as resp:
            data = json.loads(resp.read().decode())
            task_id = data["data"]["task_id"]
    except Exception as e:
        print(f"  ACE-Step submission failed: {e}", file=sys.stderr)
        return None

    # Poll
    start = time.time()
    while time.time() - start < 300:
        body = {"task_id_list": [task_id]}
        req = urllib.request.Request(
            f"{ACE_STEP_URL}/query_result",
            data=json.dumps(body).encode(),
            headers={"Content-Type": "application/json"},
            method="POST",
        )
        try:
            with urllib.request.urlopen(req, timeout=60) as resp:
                data = json.loads(resp.read().decode())
        except Exception:
            time.sleep(5)
            continue
        task = data["data"][0]
        if task["status"] == 1:
            result = json.loads(task["result"])
            audio_url = result[0]["file"]
            out_path = Path(f"station-ids/.tmp-sting-{task_id[:8]}.mp3")
            urllib.request.urlretrieve(f"{ACE_STEP_URL}{audio_url}", out_path)
            return out_path
        elif task["status"] == 2:
            print(f"  ACE-Step task failed", file=sys.stderr)
            return None
        elapsed = int(time.time() - start)
        print(f"  Generating sting... ({elapsed}s)", flush=True)
        time.sleep(5)

    print("  ACE-Step timed out", file=sys.stderr)
    return None


# ---------------------------------------------------------------------------
# TTS
# ---------------------------------------------------------------------------

def generate_tts(text, voice, output_path, intensity=0.3):
    """Generate TTS via announce.py."""
    cmd = [
        str(KOKORO_PYTHON),
        str(ANNOUNCE_SCRIPT),
        "--text", text[:300],
        "--output", str(output_path),
        "--voice", voice,
        "--intensity", str(intensity),
    ]
    try:
        subprocess.run(cmd, check=True, capture_output=True, text=True, timeout=300)
        return output_path.exists() and output_path.stat().st_size > 100
    except (subprocess.CalledProcessError, FileNotFoundError, subprocess.TimeoutExpired):
        return False


# ---------------------------------------------------------------------------
# Mix
# ---------------------------------------------------------------------------

def mix_sting_and_voice(sting_path, voice_path, out_path):
    """Mix musical sting with TTS voiceover using ffmpeg.
    Voiceover starts 0.5s in, sting ducks under voice, fades out at end."""
    try:
        cmd = [
            "ffmpeg", "-y",
            "-i", str(sting_path),
            "-i", str(voice_path),
            "-filter_complex",
            # Voice delayed 0.5s, sting ducks: volume drops when voice is present
            "[0:a]volume=0.6[sting];"
            "[1:a]adelay=500|500,volume=1.2[voice];"
            "[sting][voice]amix=inputs=2:duration=longest:dropout_transition=2,"
            "afade=t=out:st=8:d=2,"
            "loudnorm=I=-16:TP=-1.5:LRA=11[out]",
            "-map", "[out]",
            "-ar", "24000",
            str(out_path),
        ]
        subprocess.run(cmd, check=True, capture_output=True, timeout=30)
        return out_path.exists()
    except Exception as e:
        print(f"  ffmpeg mix failed: {e}", file=sys.stderr)
        return False


def mix_sting_only(sting_path, out_path):
    """Just apply effects to the sting, no voiceover."""
    try:
        cmd = [
            "ffmpeg", "-y",
            "-i", str(sting_path),
            "-af",
            "volume=0.7,afade=t=out:st=8:d=2,loudnorm=I=-16:TP=-1.5:LRA=11",
            "-ar", "24000",
            str(out_path),
        ]
        subprocess.run(cmd, check=True, capture_output=True, timeout=30)
        return out_path.exists()
    except Exception:
        return False


