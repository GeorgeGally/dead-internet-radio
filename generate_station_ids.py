def call_llm(system_prompt, user_message, json_mode=False):
    import json
    import time
    import urllib.request
    import urllib.error

    headers = {
        "Authorization": f"Bearer {OPENROUTER_API_KEY}",
        "Content-Type": "application/json",
    }
    body = {
        "model": OPENROUTER_MODEL,
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_message},
        ],
        "temperature": 0.9,
    }
    if json_mode:
        body["response_format"] = {"type": "json_object"}
    for attempt in range(3):
        try:
            req = urllib.request.Request(
                OPENROUTER_URL,
                data=json.dumps(body).encode(),
                headers=headers,
                method="POST",
            )
            with urllib.request.urlopen(req, timeout=120) as resp:
                result = json.loads(resp.read().decode())
                content = result["choices"][0]["message"]["content"]
                if content is None:
                    raise ValueError("LLM returned null content")
                return content
        except urllib.error.HTTPError as e:
            if e.code == 429:
                wait = 3 * (attempt + 1)
                print(f"  Rate limited, retrying in {wait}s...", flush=True)
                time.sleep(wait)
                continue
            print(f"  LLM call failed ({e.code})", flush=True)
            break
        except Exception as e:
            print(f"  {e}", flush=True)
            break
    return None


def generate_texts(prompt_path, count):
    """Generate station IDs and jingles via LLM."""
    if not OPENROUTER_API_KEY:
        print("  No OPENROUTER_API_KEY, using fallback texts", flush=True)
        return FALLBACK_IDS, FALLBACK_JINGLES

    prompt = Path(prompt_path).read_text().strip()
    print("  Generating texts via LLM...", flush=True)
    user_msg = (
        f"Generate station IDs and jingles for Dead Internet Radio.\n"
        f"I need {count} total items (mix of IDs and jingles). "
        f"Produce enough variety to fill that count."
    )
    raw = call_llm(prompt, user_msg, json_mode=True)
    if raw is None:
        print("  LLM failed, using fallback texts", flush=True)
        return FALLBACK_IDS, FALLBACK_JINGLES

    try:
        data = json.loads(raw)
        ids = data.get("ids", [])
        jingles = data.get("jingles", [])
        if len(ids) < 3 or len(jingles) < 2:
            raise ValueError("Too few items")
        print(f"  Got {len(ids)} IDs, {len(jingles)} jingles", flush=True)
        return ids, jingles
    except Exception as e:
        print(f"  LLM parse failed ({e}), using fallback texts", flush=True)
        return FALLBACK_IDS, FALLBACK_JINGLES


def generate_sting_caption():
    """Use LLM to generate a musical sting description for ACE-Step."""
    if not OPENROUTER_API_KEY:
        return _fallback_sting_caption()

    system = (
        "You generate short musical sting descriptions for a dark, mechanical "
        "radio station. Output a JSON object with a 'caption' field containing "
        "a technical sound design description (no poetry, no metaphors). "
        "2-3 sentences. Name synths, frequencies, drum sounds, effects."
    )
    user = (
        "Generate a short musical sting (5-15 seconds) for a radio station ID. "
        "Think: synth stab, bass drop, pad swell, filter sweep, or dark drone. "
        "Technical description only — concrete synth types, frequency ranges, "
        "drum sounds, effects. Output JSON: {\"caption\": \"...\"}"
    )
    raw = call_llm(system, user, json_mode=True)
    if raw is None:
        return _fallback_sting_caption()
    try:
        data = json.loads(raw)
        return data.get("caption", "") or _fallback_sting_caption()
    except Exception:
        return _fallback_sting_caption()


