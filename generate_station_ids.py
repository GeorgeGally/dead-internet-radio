"""Generate station ID stabs with musical stings for Dead Internet Radio.

Each station ID gets:
1. A musical sting generated by ACE-Step (5-15s synth/bass/pad)
2. A TTS voiceover via Kokoro
3. Mixed together with ffmpeg

Run requires ACE-Step running at localhost:8001.
"""
